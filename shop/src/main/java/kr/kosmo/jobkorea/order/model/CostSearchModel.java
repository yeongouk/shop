package kr.kosmo.jobkorea.order.model;

public class CostSearchModel {
	
	
	private int pc_no;
	private int use_time;
	private String loginID;
	private int pcuseamt;
	private int foodamt;
	private int totalamt;
	private String cost_date;
	private String reg_date;
	
	
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public int getPc_no() {
		return pc_no;
	}
	public void setPc_no(int pc_no) {
		this.pc_no = pc_no;
	}
	public int getUse_time() {
		return use_time;
	}
	public void setUse_time(int use_time) {
		this.use_time = use_time;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public int getPcuseamt() {
		return pcuseamt;
	}
	public void setPcuseamt(int pcuseamt) {
		this.pcuseamt = pcuseamt;
	}
	public int getFoodamt() {
		return foodamt;
	}
	public void setFoodamt(int foodamt) {
		this.foodamt = foodamt;
	}
	public int getTotalamt() {
		return totalamt;
	}
	public void setTotalamt(int totalamt) {
		this.totalamt = totalamt;
	}
	public String getCost_date() {
		return cost_date;
	}
	public void setCost_date(String cost_date) {
		this.cost_date = cost_date;
	}

	

}
