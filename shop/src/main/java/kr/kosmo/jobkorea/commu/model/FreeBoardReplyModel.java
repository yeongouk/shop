package kr.kosmo.jobkorea.commu.model;

public class FreeBoardReplyModel {
	
	private int rno;
	private String id;
	private String reply_content;
	private int reply_date;
	private String article_num;
	
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReply_content() {
		return reply_content;
	}
	public void setReply_content(String reply_content) {
		this.reply_content = reply_content;
	}
	public int getReply_date() {
		return reply_date;
	}
	public void setReply_date(int reply_date) {
		this.reply_date = reply_date;
	}
	public String getArticle_num() {
		return article_num;
	}
	public void setArticle_num(String article_num) {
		this.article_num = article_num;
	}
	
	

}
