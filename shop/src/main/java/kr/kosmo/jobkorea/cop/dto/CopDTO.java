package kr.kosmo.jobkorea.cop.dto;

import org.apache.ibatis.type.Alias;

@Alias("CoptDTO")
public class CopDTO {

	private String loginId;
	private String password;
	private String name;
	private String email;
	private String email_cop;
	private String tel1;
	private String tel2;
	private String tel3;
	@Override
	public String toString() {
		return "CoptDTO [loginId=" + loginId + ", password=" + password + ", name=" + name + ", email="
				+ email + ", email_cop=" + email_cop + ", tel1=" + tel1 + ", tel2=" + tel2
				+ ", tel3=" + tel3 + "]";
	}
	public String getLoginId() {
		return loginId;
	}
	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getEmail_cop() {
		return email_cop;
	}
	public void setEmail_cop(String email_cop) {
		this.email_cop = email_cop;
	}

	public String getTel1() {
		return tel1;
	}
	public void setTel1(String tel1) {
		this.tel1 = tel1;
	}
	public String getTel2() {
		return tel2;
	}
	public void setTel2(String tel2) {
		this.tel2 = tel2;
	}
	public String getTel3() {
		return tel3;
	}
	public void setTel3(String tel3) {
		this.tel3 = tel3;
	}
	
	public CopDTO(String loginId, String password, String name, String email, String email_cop,
			String tel1, String tel2, String tel3) {
		super();
		this.loginId = loginId;
		this.password = password;
		this.name = name;
		this.email = email;
		this.email_cop = email_cop;
		this.tel1 = tel1;
		this.tel2 = tel2;
		this.tel3 = tel3;
		
	}
	public CopDTO() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
	
}
